public class codHerencia {
         protected String reino;
         protected String filo;
         protected String clase;
         protected String orden;
         protected String familia;
         protected String genero;
         protected String especie;

         public codHerencia(String reino, String filo, String clase, String orden,String familia, String genero, String especie){
             this.reino=reino;
             this.filo=filo;
             this.clase=clase;
             this.orden=orden;
             this.familia=familia;
             this.genero=genero;
             this.especie=especie;
         }
}



